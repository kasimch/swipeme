package com.swipeme.domain;

public class TimeSheetEntries {

	private String empId;
	private String holderNumber;
	private String inTime;
	private String outTime;
	private String startDate;
	private String hoursPresent;
	private String reason;
	private String empName;
	
	
	
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	public String getEmpId() {
		return empId;
	}
	public void setEmpId(String empId) {
		this.empId = empId;
	}
	public String getHolderNumber() {
		return holderNumber;
	}
	public void setHolderNumber(String holderNumber) {
		this.holderNumber = holderNumber;
	}
	public String getInTime() {
		return inTime;
	}
	public void setInTime(String inTime) {
		this.inTime = inTime;
	}
	public String getOutTime() {
		return outTime;
	}
	public void setOutTime(String outTime) {
		this.outTime = outTime;
	}
	public String getStartDate() {
		return startDate;
	}
	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}
	public String getHoursPresent() {
		return hoursPresent;
	}
	public void setHoursPresent(String hoursPresent) {
		this.hoursPresent = hoursPresent;
	}
	public String getReason() {
		return reason;
	}
	public void setReason(String reason) {
		this.reason = reason;
	}
	
	
	

}
